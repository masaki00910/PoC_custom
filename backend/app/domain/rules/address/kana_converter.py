"""全角カナ → 半角カナ変換 (F-05-008 郵政住所マスタフォールバック専用)。

元 Power Automate フロー (F-05-008 `MappingList` Compose) のマッピング辞書を
忠実に Python に移植したもの。マスタ値との照合互換を保つため、Python 標準の
unicodedata.normalize や jaconv 等の汎用変換は使わない。

未マップの文字 (漢字・数字・空白等) はそのまま保持する。
"""
from __future__ import annotations

from collections.abc import Mapping

# 元 PA フローの MappingList Compose と完全一致 (78 エントリ)。
_FULLWIDTH_TO_HALFWIDTH_KANA: Mapping[str, str] = {
    "ア": "ｱ", "イ": "ｲ", "ウ": "ｳ", "エ": "ｴ", "オ": "ｵ",
    "カ": "ｶ", "キ": "ｷ", "ク": "ｸ", "ケ": "ｹ", "コ": "ｺ",
    "サ": "ｻ", "シ": "ｼ", "ス": "ｽ", "セ": "ｾ", "ソ": "ｿ",
    "タ": "ﾀ", "チ": "ﾁ", "ツ": "ﾂ", "テ": "ﾃ", "ト": "ﾄ",
    "ナ": "ﾅ", "ニ": "ﾆ", "ヌ": "ﾇ", "ネ": "ﾈ", "ノ": "ﾉ",
    "ハ": "ﾊ", "ヒ": "ﾋ", "フ": "ﾌ", "ヘ": "ﾍ", "ホ": "ﾎ",
    "マ": "ﾏ", "ミ": "ﾐ", "ム": "ﾑ", "メ": "ﾒ", "モ": "ﾓ",
    "ヤ": "ﾔ", "ユ": "ﾕ", "ヨ": "ﾖ",
    "ラ": "ﾗ", "リ": "ﾘ", "ル": "ﾙ", "レ": "ﾚ", "ロ": "ﾛ",
    "ワ": "ﾜ", "ヲ": "ｦ", "ン": "ﾝ",
    "ガ": "ｶﾞ", "ギ": "ｷﾞ", "グ": "ｸﾞ", "ゲ": "ｹﾞ", "ゴ": "ｺﾞ",
    "ザ": "ｻﾞ", "ジ": "ｼﾞ", "ズ": "ｽﾞ", "ゼ": "ｾﾞ", "ゾ": "ｿﾞ",
    "ダ": "ﾀﾞ", "ヂ": "ﾁﾞ", "ヅ": "ﾂﾞ", "デ": "ﾃﾞ", "ド": "ﾄﾞ",
    "バ": "ﾊﾞ", "ビ": "ﾋﾞ", "ブ": "ﾌﾞ", "ベ": "ﾍﾞ", "ボ": "ﾎﾞ",
    "パ": "ﾊﾟ", "ピ": "ﾋﾟ", "プ": "ﾌﾟ", "ペ": "ﾍﾟ", "ポ": "ﾎﾟ",
    "ァ": "ｧ", "ィ": "ｨ", "ゥ": "ｩ", "ェ": "ｪ", "ォ": "ｫ",
    "ャ": "ｬ", "ュ": "ｭ", "ョ": "ｮ",
    "ッ": "ｯ",
}


def to_halfwidth_kana(text: str) -> str:
    """全角カナを半角カナに変換する (F-05-008 のマッピング辞書に厳密準拠)。

    PA フローの `chunk(...,1)` → `MappingList[item()]` → `join(...)` を
    1 文字単位の置換で素直に再現する。マッピングに無い文字 (漢字・数字・記号・
    空白・ヴ等) はそのまま保持する。
    """
    return "".join(_FULLWIDTH_TO_HALFWIDTH_KANA.get(ch, ch) for ch in text)
